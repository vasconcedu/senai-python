# Este programa calcula a area de um retangulo

# Eele recebe como entrada a base e a altura do retangulo
# e imprime a area na tela

base = float(input("Digite a base do retangulo: "))
altura = float(input("Digite a altura do retangulo: "))

# Calcula a area do retangulo
area = base * altura

# Imprime a area do retangulo
print("A area do retangulo eh: {}".format(area))
