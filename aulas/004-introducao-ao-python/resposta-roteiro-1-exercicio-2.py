# Este programa le dois numeros e imprime na tela: 
# - A soma deles 
# - A subtracao deles
# - A multiplicacao deles
# - A divisao deles

primeiro_numero = int(input("Digite o primeiro numero: "))
segundo_numero = int(input("Digite o segundo numero: "))

soma = primeiro_numero + segundo_numero
subtracao = primeiro_numero - segundo_numero
multiplicacao = primeiro_numero * segundo_numero
divisao = primeiro_numero / segundo_numero

print("A soma eh: {}".format(soma))
print("A subtracao eh: {}".format(subtracao))
print("A multiplicacao eh: {}".format(multiplicacao))
print("A divisao eh: {}".format(divisao))
