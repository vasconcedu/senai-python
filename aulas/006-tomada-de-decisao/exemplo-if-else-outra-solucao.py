# Este programa utiliza uma tomada de decisao if-else para 
#   verificar se uma string recebida como entrada eh igual 
#   a "Python"

s = input("Digite uma string: ")

# *** ATENCAO: NOVO OPERADOR *** 
# O operador != compara se dois valores sao diferentes
if s != "Python":
    print("A string nao eh igual a 'Python'")
else:
    print("A string eh igual a 'Python'")
